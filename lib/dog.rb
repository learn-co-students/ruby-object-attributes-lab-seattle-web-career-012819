class Dog
  def name
  # method reads the name of the dog from an instance variable @name
    @name
  end

  def name=(name)
  # method writes the name of the dog to an instance variable @name
    @name = name
  end

  def breed
  # method reads breed
    @breed
  end

  def breed=(breed)
  # method writes breed
    @breed = breed
  end
end
